package pc;

public class Pc {
	String cor, marca, monitor, mouse, liga, desliga, trava, armazena;

	public String getCor() {
		return cor;
	}
	public void setCor (String cor) {
		this.cor = cor;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca (String marca) {
		this.marca = marca;
	}
	public String getMonitor() {
		return monitor;
	}
	public void setMonitor(String monitor) {
		this.monitor = monitor;
	}
	public String getMouse() {
		return mouse;
	}
	public void setMouse (String mouse) {
		this.mouse = mouse;
	}
	public String getLiga() {
		return liga;
	}
	public void setLiga (String liga) {
		this.liga = liga;
	}
	public String getDesliga() {
		return desliga;
	}
	public void setDesliga (String desliga) {
		this.desliga = desliga;
	}
	public String getTrava() {
		return trava;
	}
	public void setTrava (String trava) {
		this.trava = trava;
	}
	public String getArmazena() {
		return armazena;
	}
	public void setArmazena (String armazena) {
		this.armazena = armazena;
	}
}
