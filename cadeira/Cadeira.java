package cadeira;

public class Cadeira {
	String cor, numRodas, material, tipo, anda, deita, abaixa, levanta;

	public String getCor() {
		return cor;
	}
	public void setCor (String cor) {
		this.cor = cor;
	}
	public String getNumRodas() {
		return numRodas;
	}
	public void setNumRodas (String numRodas) {
		this.numRodas = numRodas;
	}
	public String getMaterial() {
		return material;
	}
	public void setMaterial (String material) {
		this.material = material;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo (String tipo) {
		this.tipo = tipo;
	}
	public String getAnda() {
		return anda;
	}
	public void setAnda (String anda) {
		this.anda = anda;
	}
	public String getDeita() {
		return deita;
	}
	public void setDeita (String deita) {
		this.deita = deita;
	}
	public String getAbaixa() {
		return abaixa;
	}
	public void setAbaixa (String abaixa) {
		this.abaixa = abaixa;
	}
	public String getLevanta() {
		return levanta;
	}
	public void setLevanta (String levanta) {
		this.levanta = levanta;
	}
}

